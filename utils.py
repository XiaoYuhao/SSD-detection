def detect_objects(priors_cxcy, predicted_locs, predicted_scores, min_score, max_overlap, top_k, n_classes):
        """
        Decipher the 8732 locations and class scores (output of ths SSD300) to detect objects.
        For each class, perform Non-Maximum Suppression (NMS) on boxes that are above a minimum threshold.
        :param predicted_locs: predicted locations/boxes w.r.t the 8732 prior boxes, a tensor of dimensions (N, 8732, 4)
        :param predicted_scores: class scores for each of the encoded locations/boxes, a tensor of dimensions (N, 8732, n_classes)
        :param min_score: minimum threshold for a box to be considered a match for a certain class
        :param max_overlap: maximum overlap two boxes can have so that the one with the lower score is not suppressed via NMS
        :param top_k: if there are a lot of resulting detection across all classes, keep only the top 'k'
        :return: detections (boxes, labels, and scores), lists of length batch_size
        """
        batch_size = predicted_locs.size(0)
        n_priors = priors_cxcy.size(0)
        predicted_scores = F.softmax(predicted_scores, dim=2)  # (N, 8732, n_classes)

        # Lists to store final predicted boxes, labels, and scores for all images
        all_images_boxes = list()
        all_images_labels = list()
        all_images_scores = list()

        assert n_priors == predicted_locs.size(1) == predicted_scores.size(1)

        for i in range(batch_size):
            # Decode object coordinates from the form we regressed predicted boxes to
            decoded_locs = cxcy_to_xy(
                gcxgcy_to_cxcy(predicted_locs[i], priors_cxcy))  # (8732, 4), these are fractional pt. coordinates
            # Lists to store boxes and scores for this image 
            image_boxes = list()
            image_labels = list()
            image_scores = list()

            max_scores, best_label = predicted_scores[i].max(dim=1)  # (8732)

            # Check for each class
            for c in range(1, n_classes):
                # Keep only predicted boxes and scores where scores for this class are above the minimum score
                tt = time.time()
                class_scores = predicted_scores[i][:, c]  # (8732)
                score_above_min_score = class_scores > min_score  # torch.uint8 (byte) tensor, for indexing
                n_above_min_score = score_above_min_score.sum().item()
                if n_above_min_score == 0:
                    continue
                class_scores = class_scores[score_above_min_score]  # (n_qualified), n_min_score <= 8732
                class_decoded_locs = decoded_locs[score_above_min_score]  # (n_qualified, 4)
                now = time.time()
                #print('min_score time:%f' %(now - tt))
                tt = now

                # Sort predicted boxes and scores by scores
                class_scores, sort_ind = class_scores.sort(dim=0, descending=True)  # (n_qualified), (n_min_score)
                class_decoded_locs = class_decoded_locs[sort_ind]  # (n_min_score, 4)
                now = time.time()
                #print("sort time:%f" %(now - tt))
                tt = now

                # Find the overlap between predicted boxes
                overlap = find_jaccard_overlap(class_decoded_locs, class_decoded_locs)  # (n_qualified, n_min_score)
                now = time.time()
                #print("overlap time:%f" %(now - tt))
                tt = now
                # Non-Maximum Suppression (NMS)

                # A torch.uint8 (byte) tensor to keep track of which predicted boxes to suppress
                # 1 implies suppress, 0 implies don't suppress
                suppress = torch.zeros((n_above_min_score), dtype=torch.uint8).to(device)  # (n_qualified)

                # Consider each box in order of decreasing scores
                for box in range(class_decoded_locs.size(0)):
                    # If this box is already marked for suppression
                    if suppress[box] == 1:
                        continue

                    # Suppress boxes whose overlaps (with this box) are greater than maximum overlap
                    # Find such boxes and update suppress indices
                    
                    suppress = torch.max(suppress, (overlap[box] > max_overlap).to(torch.uint8))
                    # The max operation retains previously suppressed boxes, like an 'OR' operation

                    # Don't suppress this box, even though it has an overlap of 1 with itself
                    suppress[box] = 0

                # Store only unsuppressed boxes for this class
                image_boxes.append(class_decoded_locs[1 - suppress])
                image_labels.append(torch.LongTensor((1 - suppress).sum().item() * [c]).to(device))
                image_scores.append(class_scores[1 - suppress])
                now = time.time()
                #print('suppress time:%f' %(now - tt))

            # If no object in any class is found, store a placeholder for 'background'
            if len(image_boxes) == 0:
                image_boxes.append(torch.FloatTensor([[0., 0., 1., 1.]]).to(device))
                image_labels.append(torch.LongTensor([0]).to(device))
                image_scores.append(torch.FloatTensor([0.]).to(device))

            # Concatenate into single tensors
            image_boxes = torch.cat(image_boxes, dim=0)  # (n_objects, 4)
            image_labels = torch.cat(image_labels, dim=0)  # (n_objects)
            image_scores = torch.cat(image_scores, dim=0)  # (n_objects)
            n_objects = image_scores.size(0)

            # Keep only the top k objects
            if n_objects > top_k:
                image_scores, sort_ind = image_scores.sort(dim=0, descending=True)
                image_scores = image_scores[:top_k]  # (top_k)
                image_boxes = image_boxes[sort_ind][:top_k]  # (top_k, 4)
                image_labels = image_labels[sort_ind][:top_k]  # (top_k)

            # Append to lists that store predicted boxes and scores for all images
            all_images_boxes.append(image_boxes)
            all_images_labels.append(image_labels)
            all_images_scores.append(image_scores)

        return all_images_boxes, all_images_labels, all_images_scores  # lists of length batch_size


def load_pretrained(model, pretrained_path):
    pretrained_dict = torch.load(pretrained_path)
    model_dict = model.state_dict()
    for k, v in model_dict.items():
        if v.shape == pretrained_dict[k].shape:
            model_dict[k] = pretrained_dict[k]
        else:
            print('size mismatch in %s. Expect %s but get %s' %(k, v.shape, pretrained_dict[k].shape))
    model.load_state_dict(model_dict)
    return model


def detector(priors_cxcy, predicted_locs, predicted_scores, min_score, max_overlap, top_k):
    '''
    Params:
        priors_cxcy: [8732, 4]
        predicted_locs: [N, 8732, 4]
        predicted_scores: [N, 8732, num_classes]
    '''
    result_list = []
    for locs, scores in zip(predicted_locs, predicted_scores):              #对每一张图片的预测值分别进行处理
        result = get_bboxes_single(priors_cxcy, locs, scores, min_score, max_overlap, top_k)
        result_list.append(result)

    return result_list    

def get_bboxes_single(anchors, predicted_locs, predicted_scores, min_score, max_overlap, top_k):
    '''
    Params:
        anchors: [8732, 4]
        predicted_locs: [8732, 4]
        predicted_scores: [8732, num_classes]
    '''
    assert anchors.size(0) == predicted_locs.size(0) == predicted_scores.size(0)
    mlvl_bboxes = []
    mlvl_scores = []

    scores = predicted_scores.softmax(-1)
    max_scores, _ = scores[:, 1:].max(dim=1)            #把背景类别的分数去掉
    #max_scores, _ = scores.max(dim=1)
    _, topk_inds = max_scores.topk(top_k)

    anchors = anchors[topk_inds, :]
    predicted_locs = predicted_locs[topk_inds, :]
    scores = scores[topk_inds, :]

    bboxes = cxcy_to_xy(gcxgcy_to_cxcy(predicted_locs, anchors))       #decode
    mlvl_bboxes.append(bboxes)
    mlvl_scores.append(scores)
    mlvl_bboxes = torch.cat(mlvl_bboxes)
    mlvl_scores = torch.cat(mlvl_scores)

    det_bboxes, det_labels = multiclass_nms(mlvl_bboxes, mlvl_scores, min_score, max_overlap)
    det_labels = det_labels + 1                         #因为之前把背景类别去掉了，要把类别+1才是真正的类别
    return det_bboxes, det_labels


def multiclass_nms(multi_bboxes, multi_scores, score_thr, threshold, max_num=-1):
    '''
    Params:
        multi_bboxes: [n, 4]
        multi_scores: [n, num_class]
    '''
    num_classes = multi_scores.size(1) - 1
    bboxes = multi_bboxes[:, None].expand(-1, num_classes, 4)
    scores = multi_scores[:, 1:]
    
    valid_mask = scores > score_thr
    bboxes = bboxes[valid_mask]
    scores = scores[valid_mask]
    labels = valid_mask.nonzero()[:, 1]

    if bboxes.numel() == 0:
        bboxes = multi_bboxes.new_zeros((0, 5))
        labels = multi_bboxes.new_zeros((0, ), dtype=torch.long)
        return bboxes, labels
    
    dets, keep = batch_nms(bboxes, scores, labels, threshold)
    if max_num > 0:
        dets = dets[:max_num]
        keep = keep[:max_num]
    
    return dets, labels[keep]


def batch_nms(bboxes, scores, inds, threshold):
    '''
    Params:
        bboxes: [n, 4]
        scores: [n]
    '''
    max_coordinate = bboxes.max()
    offset = inds.to(bboxes) * (max_coordinate + 1)
    bboxes_for_nms = bboxes + offset[:, None]
    dets, keep = nms(torch.cat([bboxes_for_nms, scores[:, None]], -1), threshold)
    bboxes = bboxes[keep]
    scores = dets[:, -1]
    return torch.cat([bboxes, scores[:, None]], -1), keep

def nms(dets, threshold):
    x1 = dets[:, 0]
    y1 = dets[:, 1]
    x2 = dets[:, 2]
    y2 = dets[:, 3]

    areas = (y2-y1) * (x2-x1)
    scores = dets[:, 4]
    keep = []

    _, order = scores.sort(0, descending=True)

    while order.numel() > 0:
        if order.numel() == 1:
            i = order.item()
            keep.append(i)
            break
        else:
            i = order[0].item()
            keep.append(i)
            
        xx1 = x1[order[1:]].clamp(min=x1[i].data)
        yy1 = y1[order[1:]].clamp(min=y1[i].data)
        xx2 = x2[order[1:]].clamp(max=x2[i].data)
        yy2 = y2[order[1:]].clamp(max=y2[i].data)
        inter = (xx2-xx1).clamp(min=0) * (yy2-yy1).clamp(min=0)

        iou = inter / (areas[i]+areas[order[1:]] - inter)
        idx = (iou <= threshold).nonzero().squeeze()
        if idx.numel() == 0:
            break
        order = order[idx+1]
    
    keep = torch.LongTensor(keep)
    return dets[keep, :], keep 

